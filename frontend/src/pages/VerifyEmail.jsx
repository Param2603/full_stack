import axios from 'axios'
import React, { useEffect, useState } from 'react'
import { useNavigate, useParams } from 'react-router'

const VerifyEmail = () => {
    const {token} = useParams()
    const [status, setStatus] = useState('Verifying...')
    const navigate = useNavigate()

    const verifyEmail = async()=>{
        try {
            const res = await axios.post(`http://localhost:8080/api/v1/user/verify/${token}`,{},)
            
            if(res.data.success){
                setStatus('✅ Email Verified Successfully')
                setTimeout(() => {
                    navigate('/login')
                },2000)
            }
        } catch (err) {
            console.log(err)
            setStatus("❌ Verification failed. Please try again")
        }
    }
    useEffect(() => {
        if (token) {
       verifyEmail()
     } else {
       setStatus('❌ Invalid verification link')
    }  //page render verifyEmail render
    },[token])   
  return (
    <div className="relative w-full min-h-screen bg-pink-100 overflow-hidden flex items-center justify-center">

        <div className="bg-white p-6 rounded-2xl shadow-md w-[90%] max-w-md text-center">
            <h2 className="text-xl font-semibold text-gray-800">{status}</h2>
       </div>
    </div>
  )
}

export default VerifyEmail